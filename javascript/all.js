/*排序*/
function sort(sortByField,sortDescending){
  let ary =filteredData;
  let array =ary.sort(function (a, b){
    if (sortDescending=="up") {
      return b[sortByField] - a[sortByField];
    } else if (sortDescending=="down"){
      return a[sortByField] - b[sortByField];
    } else {
      return;
    }
  });    
  filteredData=array;
  render(filteredData);
}